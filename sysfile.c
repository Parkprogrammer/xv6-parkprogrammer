int sys_mmap(void) {
    
    int i_addr;
    int length, prot, flags, fd = -1, offset = 0;
    struct file *f;

    // 인자들을 가져옵니다.
    if (argint(0, &i_addr) < 0 || argint(1, &length) < 0 || argint(2, &prot) < 0 || argint(3, &flags) < 0) {
        
        return 0; // 인자가 부족하면 실패, 0을 반환합니다.
    }
    
    uint addr = (uint)i_addr;

    // fd 인자를 가져옵니다. fd가 제공되지 않으면 fd는 기본값 -1입니다.
    if (argint(4, &fd) < 0) {
        fd = -1;
    }
    
    // offset 인자를 가져옵니다. offset이 제공되지 않으면 offset은 기본값 0입니다.
    if (argint(5, &offset) < 0) {
        offset = 0;
    }
    
    // anonymous 매핑이 아닌 경우에 파일 디스크립터를 찾습니다.
    if (fd != -1) {
        if (argfd(4, &fd, &f) < 0) {
            return 0; // 파일 디스크립터가 유효하지 않으면 실패, 0을 반환합니다.
        }
        filedup(f); // 파일 디스크립터의 참조 수를 증가시킵니다.
    } else if (fd == -1 && ((flags == MAP_ANONYMOUS) || (flags == (MAP_ANONYMOUS | MAP_POPULATE)))) {
        // fd가 -1이면서 MAP_ANONYMOUS 플래그가 설정된 경우 처리
        if (offset != 0) {
          
            return 0;  // offset이 0이 아닌 경우 에러 처리
        }
        
    } else {
        return 0; // 기타 경우 실패
    }
    // mmap 함수를 호출하여 매핑을 수행합니다.
    return (int)mmap(addr, length, prot, flags, fd, offset);
}


